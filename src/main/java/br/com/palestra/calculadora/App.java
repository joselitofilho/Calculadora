        jButtonDividir.addActionListener(new ActionListener() {
			
			public void actionPerformed(ActionEvent arg0) {
				double v = Double.parseDouble(textFieldVisor.getText());
				if (primeiroClick) {
					valorAtual = v;
					textFieldVisor.setText("0");
					primeiroClick = false;
				} else {
					String resultado = "";
					
					if (v == 0.0) {
						resultado = "Divisão opr zero.";
					}
					else {
						valorAtual = Operacao.Divisao(valorAtual, v);
						resultado = "" + valorAtual;
					}
					
					textFieldVisor.setText(resultado);
				}
			}
		});

