        jButtonVezes.addActionListener(new ActionListener() {
			
			public void actionPerformed(ActionEvent arg0) {
				double v = Double.parseDouble(textFieldVisor.getText());
				if (primeiroClick) {
					valorAtual = v;
					textFieldVisor.setText("0");
					primeiroClick = false;
				} else {
					String resultado = "";
						valorAtual = Operacao.multiplicacao(valorAtual, v);
						resultado = "" + valorAtual;
					textFieldVisor.setText(resultado);
				}
			}
		});
        
